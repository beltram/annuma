#[derive(Debug, clap::Subcommand, strum::Display)]
pub enum Department {
    #[clap(about = "1-Ain")]
    Ain { #[arg(value_enum)] commune: Ain },
    #[clap(about = "2-Aisne")]
    Aisne { #[arg(value_enum)] commune: Aisne },
    #[clap(about = "3-Allier")]
    Allier { #[arg(value_enum)] commune: Allier },
    #[clap(about = "4-AlpesDeHauteProvence")]
    AlpesDeHauteProvence { #[arg(value_enum)] commune: AlpesDeHauteProvence },
    #[clap(about = "5-HautesAlpes")]
    HautesAlpes { #[arg(value_enum)] commune: HautesAlpes },
    #[clap(about = "6-AlpesMaritimes")]
    AlpesMaritimes { #[arg(value_enum)] commune: AlpesMaritimes },
    #[clap(about = "7-Ardeche")]
    Ardeche { #[arg(value_enum)] commune: Ardeche },
    #[clap(about = "8-Ardennes")]
    Ardennes { #[arg(value_enum)] commune: Ardennes },
    #[clap(about = "9-Ariege")]
    Ariege { #[arg(value_enum)] commune: Ariege },
    #[clap(about = "10-Aube")]
    Aube { #[arg(value_enum)] commune: Aube },
}
impl Department {
    pub fn number(&self) -> u32 {
        match self {
            Department::Ain { .. } => 1u32,
            Department::Aisne { .. } => 2u32,
            Department::Allier { .. } => 3u32,
            Department::AlpesDeHauteProvence { .. } => 4u32,
            Department::HautesAlpes { .. } => 5u32,
            Department::AlpesMaritimes { .. } => 6u32,
            Department::Ardeche { .. } => 7u32,
            Department::Ardennes { .. } => 8u32,
            Department::Ariege { .. } => 9u32,
            Department::Aube { .. } => 10u32,
        }
    }
    pub fn commune(&self) -> Box<&dyn std::fmt::Display> {
        match self {
            Department::Ain { commune } => Box::new(commune),
            Department::Aisne { commune } => Box::new(commune),
            Department::Allier { commune } => Box::new(commune),
            Department::AlpesDeHauteProvence { commune } => Box::new(commune),
            Department::HautesAlpes { commune } => Box::new(commune),
            Department::AlpesMaritimes { commune } => Box::new(commune),
            Department::Ardeche { commune } => Box::new(commune),
            Department::Ardennes { commune } => Box::new(commune),
            Department::Ariege { commune } => Box::new(commune),
            Department::Aube { commune } => Box::new(commune),
        }
    }
}
